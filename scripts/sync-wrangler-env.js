/**
 * Writes NEXT_PUBLIC_* vars from wrangler.jsonc into .env.production.local
 * so Next.js inlines them into the client bundle at build time.
 */
const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const wranglerPath = path.join(root, 'wrangler.jsonc');
const outPath = path.join(root, '.env.production.local');

if (!fs.existsSync(wranglerPath)) {
  console.warn('sync-wrangler-env: wrangler.jsonc not found, skipping.');
  process.exit(0);
}

const raw = fs.readFileSync(wranglerPath, 'utf8');
const publicVars = [];

for (const match of raw.matchAll(/"(NEXT_PUBLIC_[A-Z0-9_]+)"\s*:\s*"([^"]*)"/g)) {
  publicVars.push([match[1], match[2]]);
}

if (publicVars.length === 0) {
  console.warn('sync-wrangler-env: no NEXT_PUBLIC_* vars in wrangler.jsonc.');
  process.exit(0);
}

const lines = [
  '# Generated by scripts/sync-wrangler-env.js — do not commit',
  ...publicVars.map(([key, value]) => `${key}=${value}`),
  '',
];

fs.writeFileSync(outPath, lines.join('\n'));
console.log(`sync-wrangler-env: wrote ${publicVars.length} vars to .env.production.local`);

for (const [key, value] of publicVars) {
  if (!process.env[key]) process.env[key] = value;
}
